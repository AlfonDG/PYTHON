contador1 = 0
#Primer bucle WHILE que hasta que no llegue a 10 no parará la secuancia de números y la mostrará por pantalla
while contador1 <= 9:

    contador1 += 1

    print(contador1, end="   ")

print("\n")

#Segundo bucle WHILE que muestre y multiplique de 2 la secuancia de números
contador2 = 1

while contador2 <= 20:

    contador2 *= 2

    print(contador2, end="    ")

print("\n")

#Tercer bucle WHILE que muestre los números a partir de 20 hasta llegar a 38 y que vaya sumando de 2 en 2
contador3 = 18

while contador3 <= 37:

    contador3 += 2

    print(contador3, end="    ")

print("\n")

#Cuatro bucle que empezando en 10 irá sumando de 4 en 4 hasta llegar a 30
contador4 = 10

while contador4 <= 31:

    print(contador4, end="   ")

    contador4 += 4

print("\n")

#Quinto bucle que empieza en 40 e irá decrementando de 5 en 5 hasta llegar a 0
contador5 = 40

while contador5 >= 0:

    print(contador5, end="     ")

    contador5 -= 5

print("\n")

